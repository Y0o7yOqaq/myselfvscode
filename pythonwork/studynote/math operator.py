# # 算术运算符
# 标准算术运算符：加(+)、减(-)、乘(*)、除(/)、整除(// 一正一负向下取整)
# 取余运算符：%(一正一负要公式：余数=被除数-除数*商)
# 幂运算符：**
print(1+1, type(1+1))
print(1-1, type(1-1))
print(1*1, type(1*1))
print(1/3, type(1/3))
print(11//3, type(11//3))
print(11 % 3, type(11 % 3))
print(2**3, type(2**3))
print('\r')
print(9//-4, type(9//-4))            # 一正一负向下取整
print(-9//4, type(-9//4))
print('\r')
print(9 % -4, type(9 % -4))           # 一正一负要公式：余数=被除数-除数*商
print(-9 % 4, type(-9 % 4))
