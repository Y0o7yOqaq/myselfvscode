public class helloworld { // 类是Java程序的基本组成单元，定义了一个公共的主类
    // {类体 类体中包含属性与方法两部分} 类体中除声明变量外不允许直接编写Java语句
    public static void main(String[] args) { /*
                                              * 定义一个公开的静态的主方法 ,(String[] args) 是一个mian方法的形式参数列表
                                              * main()必须声明为public static void
                                              * public 是main()方法的*权限修饰符
                                              * static 是main()方法的*静态修饰符
                                              * void 是main()方法的*返回值修饰符
                                              * (String[] args) 是一个mian方法的形式参数列表 是一个字符串类型的数组
                                              * main方法是一个程序的执行入口
                                              * 每个程序中都必须有一个mian方法
                                              * 含有main方法的类称为主类
                                              */

        System.out.println("Hello world!"); /* java语句 */
    }
}
